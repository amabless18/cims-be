    var toggleBtn = document.getElementById("toggleBtn");
    var isExpanded = false;
    
    toggleBtn.addEventListener("click", function () {
      if (isExpanded) {
        lessText.classList.add("d-none");
        moreText.classList.remove("d-none");
        toggleBtn.innerText = "Read More";
        isExpanded = false;
      } else {
